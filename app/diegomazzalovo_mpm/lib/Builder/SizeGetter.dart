import '../custom_widgets.dart';

class SizeGetter {

    static double getAppBarHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/8;
    }

    static double getFullWidth(BuildContext context) {
        return MediaQuery.of(context).size.width;
    }

    static double getAppBarIconWidth(BuildContext context) {
        return MediaQuery.of(context).size.width/10;
    }

    static double getAppBarExternalIconWidth(BuildContext context) {
        return MediaQuery.of(context).size.width/7;
    }

    static double getTipsDialogHeight(BuildContext context) {
        return MediaQuery.of(context).size.height / 1.2;
    }

    static double getInternalTipsButtonIconSize(BuildContext context) {
        return MediaQuery.of(context).size.width/12;
    }

    static double getInternalTipsButtonCircleDimSize(BuildContext context) {
        return MediaQuery.of(context).size.width/6;
    }

    static double getCharacterTileHeight(BuildContext context) {
        return MediaQuery.of(context).size.height / 4;
    }

    static double getAppBarTextWidth(BuildContext context) {
        return MediaQuery.of(context).size.width/1.6;
    }

    static double getExpansiontileHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/8;
    }

    static double getItemsTextWidth(BuildContext context) {
        return MediaQuery.of(context).size.width/2.2;
    }

    static double getAddCharSize(BuildContext context) {
        return MediaQuery.of(context).size.width/4;
    }

    static double getAddCharClassSelectionHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/12;
    }

    static double getAddCharPopupHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/2;
    }

    static double getDiceTextFieldheight(BuildContext context) {
        return MediaQuery.of(context).size.height/3;
    }

    static double getYesNoHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/10;
    }

    static double getYesNoWidth(BuildContext context) {
        return MediaQuery.of(context).size.width/2;
    }

    static double getYesNoDialogHeight(BuildContext context) {
        return MediaQuery.of(context).size.height/2;
    }

    static double getHomeButtonHeight(BuildContext context) {
        return MediaQuery.of(context).size.height / 7;
    }







    static double getAppBarInternalPadding(BuildContext context) {
        return MediaQuery.of(context).size.height/35;
    }

    static double getMiniPadding(BuildContext context) {
        return MediaQuery.of(context).size.width/40;
    }

    static double getItemDescriptionPadding(BuildContext context) {
        return MediaQuery.of(context).size.width/15;
    }









    static double getSmallTextSize(BuildContext context) {
        return MediaQuery.of(context).size.height / 40;
    }

    static double getNormalTextSize(BuildContext context) {
        return MediaQuery.of(context).size.height / 20;
    }

    static double getMediumTextSize(BuildContext context) {
        return MediaQuery.of(context).size.height / 30;
    }

    static double getMenuTextSize(BuildContext context) {
        return MediaQuery.of(context).size.height / 15;
    }

    static double getBigTextSize(BuildContext context) {
        return MediaQuery.of(context).size.height / 12;
    }

}